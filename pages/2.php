								<p> 2) определяет порядок и условия электронного документооборота;</p>

								<p> 3) определяет полномочия уполномоченного органа;</p>

								<p> 4) утверждает правила проведения аккредитации удостоверяющих центров;</p>

								<p> 4-1) утверждает правила выдачи, хранения, отзыва регистрационных свидетельств и подтверждения принадлежности и действительности открытого ключа электронной цифровой подписи корневым удостоверяющим центром Республики Казахстан, удостоверяющим центром государственных органов и национальным удостоверяющим центром Республики Казахстан;</p>

