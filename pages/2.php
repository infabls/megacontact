								<p> 6) разрабатывает и утверждает типовое положение удостоверяющего центра;</p>

								<p> 7) разрабатывает и утверждает формы обязательной ведомственной отчетности, проверочных листов критерии оценки степени риска, полугодовые планы проведения проверок в соответствии с Законом Республики Казахстан «О государственном контроле и надзоре в Республике Казахстан»;</p>

								<p> 8) разрабатывает правила выдачи, хранения, отзыва регистрационных свидетельств и подтверждения принадлежности и действительности открытого ключа электронной цифровой подписи корневым удостоверяющим центром Республики Казахстан, удостоверяющим центром государственных органов и национальным удостоверяющим центром Республики Казахстан;</p>

