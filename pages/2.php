								<h3>Глава 1. Общие положения</h3>

								<p><strong>Статья 1. Основные понятия, используемые в настоящем Законе</strong></p>

								<p>В настоящем Законе используются следующие основные понятия:</p>

								<p> 1) удостоверяющий центр — юридическое лицо, удостоверяющее соответствие открытого ключа электронной цифровой подписи закрытому ключу электронной цифровой подписи, а также подтверждающее достоверность регистрационного свидетельства;</p>

								<p>2) аккредитация удостоверяющего центра — официальное признание уполномоченным органом компетентности удостоверяющего центра в оказании услуг;</p>

								<p> 3) исключен Законом РК от 10.07.2012 № 34-V (вводится в действие со дня его первого официального опубликования);</p>

								<p> 4) исключен Законом РК от 10.07.2012 № 34-V (вводится в действие со дня его первого официального опубликования);</p>

								<p> 5) исключен Законом РК от 10.07.2012 № 34-V (вводится в действие со дня его первого официального опубликования);</p>

								<p> 6) подписывающее лицо — физическое или юридическое лицо, правомерно владеющее закрытым ключом электронной цифровой подписи и обладающее правом на ее использование на электронном документе;</p>

								<p> 6-1) государственная техническая служба — республиканское государственное предприятие на праве хозяйственного ведения, созданное по решению Правительства Республики Казахстан;</p>

								<p> 6-1) электронная копия документа - документ, полностью воспроизводящий вид и информацию (данные) подлинного документа в электронно-цифровой форме, удостоверенный электронной цифровой подписью заявителя или лица, обладающего полномочиями на удостоверение данного документа, либо уполномоченного работника центра обслуживания населения, на основании письменного согласия получателя государственной услуги, данного в момент его личного присутствия;</p>

								<p> 7) регистрационное свидетельство — документ на бумажном носителе или электронный документ, выдаваемый удостоверяющим центром для подтверждения соответствия электронной цифровой подписи требованиям, установленным настоящим Законом;</p>

								<p> 8) владелец регистрационного свидетельства — физическое или юридическое лицо, на имя которого выдано регистрационное свидетельство, правомерно владеющее закрытым ключом, соответствующим открытому ключу, указанному в регистрационном свидетельстве;</p>

								<p> 9) уполномоченный орган — государственный орган, осуществляющий руководство в сфере информатизации;</p>

								<p> 10) электронный документ — документ, в котором информация представлена в электронно-цифровой форме и удостоверена посредством электронной цифровой подписи;</p>

								<p> 11) система электронного документооборота — система обмена электронными документами, отношения между участниками которой регулируются настоящим Законом и иными нормативными правовыми актами Республики Казахстан</p>;

								<p> 12) участник системы электронного документооборота — физическое или юридическое лицо, государственный орган или должностное лицо, участвующие в процессах сбора, обработки, хранения, передачи, поиска и распространения электронных документов;</p>

								<p> 13) электронная цифровая подпись — набор электронных цифровых символов, созданный средствами электронной цифровой подписи и подтверждающий достоверность электронного документа, его принадлежность и неизменность содержания;</p>

								<p> 14) средства электронной цифровой подписи - совокупность программных и технических средств, используемых для создания и проверки подлинности электронной цифровой подписи;</p>

								<p> 15) открытый ключ электронной цифровой подписи - последовательность электронных цифровых символов, доступная любому лицу и предназначенная для подтверждения подлинности электронной цифровой подписи в электронном документе;</p>

								<p> 16) закрытый ключ электронной цифровой подписи — последовательность электронных цифровых символов, известная владельцу регистрационного свидетельства и предназначенная для создания электронной цифровой подписи с использованием средств электронной цифровой подписи.</p>

								<p><em>Сноска. Статья 1 в редакции Закона РК от 15.07.2010 (порядок введения в действие см. ст. 2); с изменениями, внесенными законами РК от 10.07.2012 № 34-V (вводится в действие со дня его первого официального опубликования); от 10.07.2012№ 36-V (вводится в действие по истечении десяти календарных дней после его первого официального опубликования).</em></p>


								<p><strong>Статья 2. Законодательство Республики Казахстан об электронном документе и электронной цифровой подписи</strong></p>

								<p> 1. Законодательство Республики Казахстан об электронном документе и электронной цифровой подписи основывается на Конституции Республики Казахстан, состоит из настоящего Закона и иных нормативных правовых актов Республики Казахстан.

								</p><p> 2. Если международным договором, ратифицированным Республикой Казахстан, установлены иные правила, чем те, которые содержатся в настоящем Законе, то применяются правила международного договора. </p>


								<p><strong>Статья 3. Использование иностранного регистрационного свидетельства и обмен электронными документами с участием иностранных физических и юридических лиц</strong></p>

								<p> 1. При регулировании правоотношений, возникающих между удостоверяющим центром и владельцем иностранного регистрационного свидетельства, применяется право государства, в котором было выдано регистрационное свидетельство, если иное не установлено соглашением сторон.</p>

								<p> 2. При обмене электронными документами с участием иностранных физических и юридических лиц применяется законодательство Республики Казахстан, если иное не установлено соглашением сторон.</p>


