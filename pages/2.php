								<p><strong>Статья 5-1. Государственный контроль в сфере электронного документа и электронной цифровой подписи</strong></p>

								<p> 1. Государственный контроль в сфере электронного документа и электронной цифровой подписи осуществляется в форме проверки и иных формах.</p>

								<p>2. Проверка осуществляется в соответствии с Законом Республики Казахстан "О государственном контроле и надзоре в Республике Казахстан". Иные формы государственного контроля осуществляются в соответствии с настоящим Законом.</p>

								<p><em>Сноска. Глава 1 дополнена статьей 5-1 в соответствии с Законом РК от 17.07.2009 N 188-IV (порядок введения в действие см. ст.2); с изменением, внесенным Законом РК от 06.01.2011 № 378-IV (вводится в действие по истечении десяти календарных дней после его первого официального опубликования).</em></p>


								<h3>Глава 2. Электронный документ</h3>

								<p><strong>Статья 6. Принципы электронного документооборота</strong></p>

								<p> Электронный документооборот осуществляется в государственных и негосударственных информационных системах на основе следующих принципов:</p>

								<p>1) функционирования различных систем электронного документооборота;</p>

								<p>2) использования электронных документов в любых сферах деятельности, где применяются информационные технологии для создания,обработки, хранения и передачи данных;</p>

								<p> 3) передачи электронных документов с использованием любых информационных систем.</p>


								<p><strong>Статья 7. Требования к электронному документообороту</strong></p>

								<p> 1. Электронный документ может быть создан, передан, сохранен и подан электронными средствами. Электронный документ, соответствующий требованиям настоящего Закона, равнозначен документу на бумажном носителе.</p>

								<p> 2. Электронный документ считается отправленным с момента его передачи по информационно-коммуникационной сети.</p>

								<p> 3. Входящий электронный документ считается поступившим после его фиксации в информационной системе адресата.</p>

								<p> 4. Уведомление о получении должно содержать данные о факте и времени получения электронного документа и его отправителе. В случае непоступления его автору считается, что документ не получен адресатом.</p>

								<p> 4-1. В случаях, установленных законодательством Республики Казахстан, для оказания государственной услуги представляется электронная копия документа.</p>

								<p> 5. Порядок электронного документооборота определяется Правительством Республики Казахстан.</p>

								<p> 6. Порядок использования, защиты, регистрации электронных документов, содержащих сведения, составляющие государственные секреты, устанавливается законодательством Республики Казахстан о государственных секретах.</p>

								<p><em>Сноска. Статья 7 в редакции Закона РК от 04.06.2009 N 162-IV (порядок введения в действие см. ст.2); с изменениями, внесенными Законом РК от 10.07.2012№ 36-V (вводится в действие по истечении десяти календарных дней после его первого официального опубликования).</em></p>


								<p><strong>Статья 8. Хранение электронных документов</strong></p>

								<p>Электронные документы хранятся в государственных и (или) негосударственных информационных системах в порядке, установленном законодательством Республики Казахстан.</p>


								<p><strong>Статья 9. Права и обязанности участника системы электронного документооборота</strong></p>

								<p> 1. Участник системы электронного документооборота вправе:</p>

								<p> 1) обратиться в удостоверяющий центр за подтверждением принадлежности и действительности открытого ключа электронной цифровой подписи, зарегистрированного данным удостоверяющим центром;</p>

								<p> 2) обслуживаться несколькими удостоверяющими центрами.</p>


								<p> 2. Участник системы электронного документооборота обязан соблюдать установленные правила электронного документооборота.</p>

								<p><em>Сноска. В статью 9 внесены изменения - Законом РК от 20 декабря 2004 г. N 13 (вводится в действие с 1 января 2005 г.).</em></p>


								<h3> Глава 3. Электронная цифровая подпись</h3>


								<p><strong>Статья 10. Использование электронной цифровой подписи</strong></p>

								<p>1. Электронная цифровая подпись равнозначна собственноручной подписи подписывающего лица и влечет одинаковые юридические последствия при выполнении следующих условий:</p>

								<p>1) удостоверена подлинность электронной цифровой подписи при помощи открытого ключа, имеющего регистрационное свидетельство;</p>

								<p>2) лицо, подписавшее электронный документ, правомерно владеет закрытым ключом электронной цифровой подписи;</p>

								<p>3) электронная цифровая подпись используется в соответствии со сведениями, указанными в регистрационном свидетельстве.</p>

								<p> 2. Закрытые ключи электронной цифровой подписи являются собственностью лиц, владеющих ими на законных основаниях. Лицо может иметь закрытые ключи электронной цифровой подписи для различных информационных систем. Закрытые ключи электронной цифровой подписи не могут быть переданы другим лицам без согласия владельцев этих ключей.</p>

								<p> 3. Подписывающее лицо вправе передавать полномочия на использование электронной цифровой подписи своему представителю в соответствии с законодательством Республики Казахстан.</p>

								<p><em>Сноска. Статья 10 с изменением, внесенным Законом РК от 15.07.2010 № 337-IV (порядок введения в действие см. ст. 2).</em></p>


								<p><strong>Статья 11. Средства электронной цифровой подписи</strong></p>

								<p>Средства электронной цифровой подписи подлежат сертификации в случаях и порядке, установленных законодательством Республики Казахстан о сертификации.</p>

								<p><strong>Статья 12. Электронная цифровая подпись в системе электронного документооборота</strong></p>

								<p> 1. Электронная цифровая подпись может использоваться должностными лицами государственных органов при удостоверении электронных документов, издаваемых ими в пределах их полномочий.</p>

								<p> 2. В негосударственных системах электронного документооборота электронная цифровая подпись используется в порядке, установленном гражданским законодательством Республики Казахстан.</p>

								<p><em>Сноска. Статья 12 с изменением, внесенным Законом РК от 15.07.2010 № 337-IV (порядок введения в действие см. ст. 2).</em></p>


								<p><strong>Статья 13. Признание иностранной электронной цифровой подписи</strong></p>

								<p> Иностранная электронная цифровая подпись, имеющая иностранное регистрационное свидетельство, признается электронной цифровой подписью на территории Республики Казахстан в соответствии с ратифицированными Республикой Казахстан международными договорами или после внесения в регистр регистрационных свидетельств.</p>


								<h3>Глава 4. Регистрационное свидетельство</h3>


								<p><strong>Статья 14. Выдача регистрационного свидетельства</strong></p>

								<p>Порядок выдачи регистрационного свидетельства определяется уполномоченным органом и является обязательным для удостоверяющего центра и участников системы электронного документооборота.</p>


								<p><strong>Статья 15. Содержание регистрационного свидетельства</strong></p>

								<p> 1. Регистрационное свидетельство должно содержать следующие сведения:</p>

								<p> 1) номер регистрационного свидетельства и срок его действия;</p>

								<p> 2) данные, позволяющие идентифицировать владельца электронной цифровой подписи;</p>

								<p> 3) открытый ключ электронной цифровой подписи;</p>

								<p> 4) данные о средствах электронной цифровой подписи, используемых для создания соответствующего закрытого ключа электронной цифровой подписи;</p>

								<p> 5) информацию о сферах применения и ограничениях применения электронной цифровой подписи;</p>

								<p> 6) реквизиты соответствующего удостоверяющего центра.</p>

								<p>2. Удостоверяющий центр по согласованию с участником системы электронного документооборота включает в регистрационное свидетельство дополнительную информацию, необходимую для электронного документооборота.</p>

								<p><em>Сноска. Статья 15 с изменением, внесенным Законом РК от 27.04.2012 № 15-V (вводится в действие по истечении десяти календарных дней после его первого официального опубликования).</em></p>


								<p><strong>Статья 16. Порядок и срок хранения регистрационных свидетельств в удостоверяющих центрах</strong></p>

								<p> 1. Копии регистрационных свидетельств хранятся в соответствующих удостоверяющих центрах в порядке, установленном уполномоченным органом.</p>

								<p> 2. Срок хранения отозванных регистрационных свидетельств в удостоверяющих центрах составляет не менее пяти лет.</p>

								<p> 3. По истечении срока, указанного в пункте 2 настоящей статьи, отозванные регистрационные свидетельства поступают на архивное хранение в порядке, установленном законодательством Республики Казахстан.</p>


								<p><strong>Статья 17. Права и обязанности владельца регистрационного свидетельства</strong></p>

								<p> 1. Владелец регистрационного свидетельства вправе требовать от удостоверяющего центра отзыва регистрационного свидетельства в случаях, если он предполагает нарушение режима доступа к закрытому ключу электронной цифровой подписи, соответствующему открытому ключу, указанному в регистрационном свидетельстве.</p>

								<p> 2. Владелец регистрационного свидетельства обязан:</p>

								<p> 1) предоставлять удостоверяющему центру достоверную информацию;</p>

								<p> 2) пользоваться закрытым ключом, соответствующим открытому ключу, указанному в регистрационном свидетельстве;</p>

								<p> 3) исключен Законом РК от 27.04.2012 № 15-V (вводится в действие по истечении десяти календарных дней после его первого официального опубликования);</p>

								<p> 4) принимать меры для защиты принадлежащего ему закрытого ключа электронной цифровой подписи от неправомерного доступа и использования, а также хранить открытые ключи в порядке, установленном законодательством Республики Казахстан.</p>

								<p><em>Сноска. Статья 17 с изменениями, внесенными законами РК от 15.07.2010 № 337-IV (порядок введения в действие см. ст. 2); от 27.04.2012 № 15-V (вводится в действие по истечении десяти календарных дней после его первого официального опубликования).</em></p>


								<p><strong>Статья 18. Отзыв регистрационного свидетельства</strong></p>

								<p> 1. Удостоверяющий центр, выдавший регистрационное свидетельство, отзывает его в следующих случаях:</p>

								<p> 1) по требованию владельца регистрационного свидетельства либо его представителя;</p>

								<p> 2) смерти владельца регистрационного свидетельства;</p>

								<p> 3) исключен Законом РК от 21.07.2011 № 468-IV (вводится в действие по истечении десяти календарных дней после его первого официального опубликования);</p>

								<p> 4) предусмотренных соглашением между удостоверяющим центром и владельцем регистрационного свидетельства;</p>

								<p> 5) по вступившему в законную силу решению суда.</p>

								<p> 2. Удостоверяющий центр отзывает регистрационное свидетельство в порядке и сроки, которые установлены законодательством Республики Казахстан.</p>

								<p> 3. При аннулировании регистрационного свидетельства удостоверяющий центр обязан уведомить владельца регистрационного свидетельства и внести изменения в регистр регистрационных свидетельств в срок не менее одного дня с момента получения соответствующей информации.</p>

								<p><em>Сноска. Статья 18 с изменением, внесенным Законом РК от 21.07.2011 № 468-IV (вводится в действие по истечении десяти календарных дней после его первого официального опубликования).</em></p>


								<p><strong>Статья 19. Признание иностранных регистрационных свидетельств</strong></p>

								<p> Иностранное регистрационное свидетельство признается наравне с регистрационным свидетельством, выданным удостоверяющим центром, действующим на территории Республики Казахстан, после внесения регистрационного свидетельства в регистр регистрационных свидетельств.</p>


								<h3>Глава 5. Удостоверяющий центр</h3>


								<p><strong>Статья 20. Деятельность удостоверяющего центра</strong></p>

								<p> 1. Удостоверяющий центр является юридическим лицом, созданным в соответствии с законодательством Республики Казахстан.</p>

								<p> 2. Удостоверяющий центр может обслуживать несколько систем электронного документооборота.</p>

								<p><em>Сноска. Статья 20 в редакции Закона РК от 15.07.2011№ 461-IV (вводится в действие с 30.01.2012).</em></p>


								<p><strong>Статья 20-1. Государственная монополия в сфере электронного документа и электронной цифровой подписи</strong></p>

								<p> 1. Государственная техническая служба осуществляет следующие виды деятельности, относящиеся к государственной монополии в сфере электронного документа и электронной цифровой подписи:</p>

								<p> 1) доверенной третьей стороны Республики Казахстан, осуществляющей подтверждение подлинности иностранной электронной цифровой подписи;</p>

								<p> 2) корневого удостоверяющего центра Республики Казахстан, осуществляющего подтверждение принадлежности и действительности открытых ключей электронной цифровой подписи удостоверяющих центров;</p>

								<p> 3) национального удостоверяющего центра Республики Казахстан, обслуживающего участников «электронного правительства», государственных и негосударственных информационных систем;</p>

								<p> 4) удостоверяющего центра государственных органов Республики Казахстан, обслуживающего участников единой системы электронного документооборота государственных органов Республики Казахстан.</p>

								<p>2. Цены на товары (работы, услуги), производимые и (или) реализуемые субъектом государственной монополии, устанавливаются Правительством Республики Казахстан.</p>

								<p><em>Сноска. Глава 5 дополнена статьей 20-1 в соответствии с Законом РК от 10.07.2012 № 34-V (вводится в действие со дня его первого официального опубликования).</em></p>


								<p><strong>Статья 21. Функции удостоверяющего центра</strong></p>

								<p> 1. Удостоверяющий центр:</p>

								<p> 1) создает ключи электронных цифровых подписей по обращению участников системы электронного документооборота с принятием мер для защиты закрытых ключей электронной цифровой подписи от неправомерного доступа;</p>

								<p> 2)выдает, регистрирует, отзывает, хранит регистрационные свидетельства, ведет регистр регистрационных свидетельств, выданных в установленном порядке;</p>

								<p> 3) осуществляет учет действующих и отозванных регистрационных свидетельств;</p>

								<p> 4) подтверждает принадлежность и действительность открытого ключа электронной цифровой подписи, зарегистрированного удостоверяющим центром в порядке, установленном законодательством Республики Казахстан;</p>

								<p> 5) исключен</p>

								<p> 2. Удостоверяющий центр обязан принимать все необходимые меры для предотвращения утери, модификации и подделки находящихся на хранении открытых ключей электронной цифровой подписи.</p>

								<p> 3. За неисполнение обязанности, предусмотренной пунктом 2 настоящей статьи, удостоверяющий центр несет ответственность в соответствии с законами Республики Казахстан.</p>

								<p><em>Сноска. В статью 21 внесены изменения - Законом РК от 20 декабря 2004 г. N 13 (вводится в действие с 1 января 2005 г.).</em></p>


								<p><strong>Статья 22. Прекращение деятельности удостоверяющего центра</strong></p>

								<p> 1. Деятельность удостоверяющего центра прекращается в порядке, установленном законодательством Республики Казахстан.</p>

								<p> 2. В случае прекращения деятельности удостоверяющий центр обязан за тридцать дней до прекращения своей деятельности проинформировать об этом всех участников обслуживаемых им систем электронного документооборота и уполномоченный орган.</p>

								<p> 3. При прекращении деятельности удостоверяющего центра выданные им регистрационные свидетельства и соответствующие ключи электронной цифровой подписи, сведения о владельцах регистрационных свидетельств передаются в другие удостоверяющие центры по согласованию с владельцем регистрационного свидетельства.</p>

								<p> 4. По истечении срока, указанного в пункте 2 настоящей статьи, регистрационные свидетельства и соответствующие ключи электронной цифровой подписи, не переданные в другие удостоверяющие центры, прекращают свое действие и подлежат хранению в соответствии с законодательством Республики Казахстан.</p>


								<p><strong>Статья 23. Защита сведений о владельцах регистрационных свидетельств, закрытых и открытых ключах электронной цифровой подписи</strong></p>

								<p> 1. Удостоверяющий центр обеспечивает защиту сведений о владельцах регистрационных свидетельств и раскрывает их в случаях, предусмотренных законодательными актами Республики Казахстан.</p>

								<p> 2. Сведения о владельцах регистрационных свидетельств, являющиеся конфиденциальными в соответствии с соглашением сторон, не включаются в общедоступный регистр регистрационных свидетельств.</p>


								<h3>Глава 6. Заключительные положения</h3>


								<p><strong>Статья 24. Ответственность за нарушение законодательства Республики Казахстан об электронном документе и электронной цифровой подписи</strong></p>

								<p> Лица, виновные в нарушении законодательства Республики Казахстан об электронном документе и электронной цифровой подписи, несут ответственность, предусмотренную законами Республики Казахстан.</p>


								<p><strong>Статья 25. Рассмотрение споров</strong></p>

								<p> Споры, возникающие при использовании электронного документа и электронной цифровой подписи, подлежат рассмотрению в судебном порядке в соответствии с законодательством Республики Казахстан.</p>

								<p><strong>Статья 26. Порядок введения в действие настоящего Закона</strong></p>

								<p> Настоящий Закон вводится в действие с 1 июля 2003 года.</p>

